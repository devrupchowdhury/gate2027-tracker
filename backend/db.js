const { Pool } = require('pg');
const pgvector = require('pgvector/pg');

const pool = new Pool({
  connectionString: process.env.DATABASE_URL,
  ssl: process.env.NODE_ENV === 'production' ? { rejectUnauthorized: false } : false,
});

async function query(text, params) {
  const client = await pool.connect();
  try {
    return (await client.query(text, params)).rows;
  } finally {
    client.release();
  }
}

async function queryOne(text, params) {
  return (await query(text, params))[0] || null;
}

async function initDB() {
  // Enable pgvector extension (Render Postgres supports this)
  await query(`CREATE EXTENSION IF NOT EXISTS vector`);

  await query(`
    CREATE TABLE IF NOT EXISTS sessions (
      id           SERIAL PRIMARY KEY,
      user_id      TEXT NOT NULL,
      date         TEXT NOT NULL,
      subject      TEXT NOT NULL,
      hours        REAL NOT NULL DEFAULT 0,
      score        REAL,
      productivity INTEGER DEFAULT 7,
      notes        TEXT DEFAULT '',
      created_at   TIMESTAMPTZ DEFAULT NOW()
    );

    CREATE TABLE IF NOT EXISTS errors (
      id           SERIAL PRIMARY KEY,
      user_id      TEXT NOT NULL,
      date         TEXT NOT NULL,
      subject      TEXT NOT NULL,
      type         TEXT NOT NULL,
      topic        TEXT DEFAULT '',
      description  TEXT DEFAULT '',
      created_at   TIMESTAMPTZ DEFAULT NOW()
    );

    CREATE TABLE IF NOT EXISTS pyqs (
      id           SERIAL PRIMARY KEY,
      user_id      TEXT NOT NULL,
      subject      TEXT NOT NULL,
      round        TEXT NOT NULL,
      accuracy     INTEGER DEFAULT 0,
      date         TEXT NOT NULL,
      created_at   TIMESTAMPTZ DEFAULT NOW(),
      UNIQUE(user_id, subject, round)
    );

    CREATE TABLE IF NOT EXISTS motivation (
      id           SERIAL PRIMARY KEY,
      user_id      TEXT NOT NULL,
      type         TEXT NOT NULL DEFAULT 'reflection',
      content      TEXT NOT NULL,
      created_at   TIMESTAMPTZ DEFAULT NOW()
    );

    -- Agent conversation memory
    CREATE TABLE IF NOT EXISTS agent_messages (
      id           SERIAL PRIMARY KEY,
      user_id      TEXT NOT NULL,
      role         TEXT NOT NULL,  -- 'user' | 'assistant'
      content      TEXT NOT NULL,
      created_at   TIMESTAMPTZ DEFAULT NOW()
    );

    -- Vector memory: stores embeddings of key facts about the user
    -- (errors, study notes, weak topics) for semantic retrieval
    CREATE TABLE IF NOT EXISTS memory_chunks (
      id           SERIAL PRIMARY KEY,
      user_id      TEXT NOT NULL,
      type         TEXT NOT NULL,  -- 'error'|'session'|'plan'|'reflection'
      content      TEXT NOT NULL,
      embedding    vector(1536),   -- text-embedding-3-small dimensions
      metadata     JSONB DEFAULT '{}',
      created_at   TIMESTAMPTZ DEFAULT NOW()
    );

    -- Daily study plans generated by the agent
    CREATE TABLE IF NOT EXISTS study_plans (
      id           SERIAL PRIMARY KEY,
      user_id      TEXT NOT NULL,
      date         TEXT NOT NULL,
      plan         JSONB NOT NULL,
      generated_at TIMESTAMPTZ DEFAULT NOW(),
      UNIQUE(user_id, date)
    );

    CREATE INDEX IF NOT EXISTS idx_sessions_user    ON sessions(user_id, date);
    CREATE INDEX IF NOT EXISTS idx_errors_user      ON errors(user_id, date);
    CREATE INDEX IF NOT EXISTS idx_pyqs_user        ON pyqs(user_id);
    CREATE INDEX IF NOT EXISTS idx_agent_msgs_user  ON agent_messages(user_id, created_at);
    CREATE INDEX IF NOT EXISTS idx_memory_user      ON memory_chunks(user_id, type);
    CREATE INDEX IF NOT EXISTS idx_plans_user       ON study_plans(user_id, date);
  `);

  // Vector similarity index (cosine distance)
  await query(`
    CREATE INDEX IF NOT EXISTS idx_memory_vector
    ON memory_chunks USING ivfflat (embedding vector_cosine_ops)
    WITH (lists = 50)
  `).catch(() => {}); // Ignore if not enough rows yet

  console.log('✅ Database ready');
}

module.exports = { query, queryOne, initDB };
